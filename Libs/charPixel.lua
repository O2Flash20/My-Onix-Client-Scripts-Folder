local function strToChar(str)
    local letters = {}
    for i = 1, #str, 1 do
        table.insert(letters, string.sub(str, i, i))
    end
    return letters
end

function charToPixel(char)
    if alphabet[char] then return alphabet[char] end
    return {{},{},{},{},{},{},{}}
end

function concatPixLetters(word)
    local pixels = {}
    for i, char in ipairs(strToChar(word)) do
        for j = 1, 7, 1 do
            table.insert(pixels, {})
            for i2, v in ipairs(charToPixel(char)[j]) do
                table.insert(pixels[j], v)
            end
        end
    end
    return pixels
end